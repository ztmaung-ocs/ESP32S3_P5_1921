static const int SQUARE_PLATE_FONT7X9_COUNT =
    sizeof(squarePlateFont7x9) / sizeof(squarePlateFont7x9[0]);

inline const uint8_t* getSquarePlateChar7x9(char c) {
    for (int i = 0; i < SQUARE_PLATE_FONT7X9_COUNT; i++) {
        if (squarePlateFont7x9[i].c == c) return squarePlateFont7x9[i].rows;
    }
    return squarePlateFont7x9[0].rows; // space if not found
}

